""""
5. Escreva a sequência de comandos necessária para o cálculo da área de um triângulo de base 9 e altura 6.
"""
base = int(input("Digite a base do triangulo: \n"))
altura = int(input("Digite a altura do triangulo \n"))

area = base * altura / 2

print("A area do triangulo e de: {} metros quadrados ".format(area))
